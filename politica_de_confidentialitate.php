<h2>M. Cookie-uri</h2>
<p>Website-ul nostru folosește cookie-uri. Cookie-ul este un fișier care conține un identificator (un șir de litere
    și
    numere), care este trimis de un server web către un browser și care este stocat de acel browser. Identificatorul
    este apoi trimis înapoi la server de fiecare dată când browser-ul solicită o pagină de la acesta. Cookie-urile
    pot
    fi cookie-uri „persistente” sau cookie-uri „de sesiune”: cookie-ul persistent va fi stocat de către browser și
    va
    rămâne valabil până la data stabilită de expirare, cu excepția cazului în care acesta este șters înainte de data
    de
    expirare; pe de altă parte, cookie-ul de sesiune va expira la sfârșitul sesiunii utilizatorului, la închiderea
    browser-ului. Cookie-urile nu conțin de obicei informații care identifică personal un utilizator, dar
    informațiile
    personale pe care le stocăm despre dvs. pot fi legate de informațiile stocate și obținute prin cookie-uri.</p>

<p class="to-check">Numele cookie-urilor pe care le folosim pe website-ul nostru și scopurile pentru care sunt utilizate
    acestea pot fi
    găsite mai jos:</p>

<p>Majoritatea browser-elor vă permit să refuzați să acceptați cookie-uri – de exemplu:</p>
<ol>
    <li>în Internet Explorer (versiunea 10) puteți bloca cookie-urile utilizând suprascrierea setărilor de
        manipulare a
        cookie-urilor, disponibile printr-un clic pe „Instrumente”, „Opțiuni de Internet”, „Confidențialitate”, apoi
        „Avansat”
    </li>
    <li>în Firefox (versiunea 24) puteți bloca toate cookie-urile făcând clic pe „Instrumente”, „Opțiuni”,
        „Confidențialitate”, selectând „Utilizați setări personalizate pentru istoric” din meniul derulant și
        debifând
        „Acceptați cookie-uri de la website-uri”
    </li>
    <li>în Chrome (versiunea 29), puteți bloca toate cookie-urile accesând meniul „Personalizare și control”, făcând
        clic pe „Setări”, „Afișați setările avansate” și pe „Setări de conținut”, apoi selectând „Blocați site-urile
        să
        trimită orice date” la rubrica „Cookie-uri”
    </li>
</ol>
<p>Blocarea tuturor cookie-urilor va avea un impact negativ asupra utilizabilității multor website-uri. Dacă blocați
    cookie-urile, nu veți putea utiliza toate funcțiile de pe website-ul nostru.</p>
<p>Puteți șterge cookie-urile deja stocate pe computer – de exemplu:</p>
<ol>
    <li>În Internet Explorer (versiunea 10), trebuie să ștergeți manual fișierele cookie (puteți găsi instrucțiuni
        despre cum puteți face acest lucru <a href="http://support.microsoft.com/kb/278835"
                                              target="_blank">aici</a> )
    </li>
    <li>În Firefox (versiunea 24), puteți șterge cookie-urile făcând clic pe „Instrumente”, „Opțiuni” și
        „Confidențialitate”, apoi selectând „Utilizați setări personalizate pentru istoric”, făcând clic pe „Afișare
        cookie-uri”, apoi pe „Ștergeți toate Cookie-urile“
    </li>
    <li>în Chrome (versiunea 29), puteți șterge toate cookie-urile accesând meniul „Personalizare și control”,
        făcând
        clic pe „Setări”, „Afișați setările avansate” și pe „Ștergeți datele de navigare”, apoi selectați „Ștergeți
        cookie-urile și alte date despre website și plugin-uri” și faceți clic pe „Ștergeți datele de navigare”.
    </li>
</ol>
<p>Ștergerea cookie-urilor va avea un impact negativ asupra utilizabilității multor website-uri.</p>

